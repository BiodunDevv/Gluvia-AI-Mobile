export const normalizeLanguage = (
  language?: string | null
): SupportedLanguage => {
  if (!language) {
    return "english";
  }

  const found = SUPPORTED_LANGUAGES.find((item) => item.value === language);
  return found?.value || "english";
};

export const translateText = (
  text: string,
  language?: string | null
): string => {
  const normalized = normalizeLanguage(language);
  return dictionaries[normalized][text] || text;
};
